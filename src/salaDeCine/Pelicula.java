package salaDeCine;

public class Pelicula {
	String titulo;
	String director;
	int duracion;
	int edad_minima;
	public Pelicula(String titulo, String director, int duracion, int edad_minima) {
		this.titulo=titulo;
		this.director=director;
		this.duracion=duracion;
		this.edad_minima=edad_minima;
	}
}
